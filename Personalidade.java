
public class Personalidade {

	String tipo;
	int resultado;
	int relacionamento;
	int resiliencia;
	int comunicacao;
	int criatividade;
	int intelTecnica;
	int comprometimento;
	int adaptacao;
	
	public Personalidade(String tipo, int resultado, int relacionamento, int resiliencia,
							int comunicacao, int criatividade, int intelTecnica, 
							int comprometimento, int adaptacao) {
		
		this.tipo = tipo;
		this.resultado = resultado;
		this.relacionamento = relacionamento;
		this.resiliencia = resiliencia;
		this.comunicacao = comunicacao;
		this.criatividade = criatividade;
		this.intelTecnica = intelTecnica;
		this.comprometimento = comprometimento;
		this.adaptacao = adaptacao;
	
	}

}
